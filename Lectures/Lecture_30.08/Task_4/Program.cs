void PrintImage(int[,] image)
{
    for (int row = 0; row < image.GetLength(0); row++)
    {
        for (int column = 0; column < image.GetLength(1); column++)
        {
            if (image[row, column] == 0) Console.Write($" ");
            else Console.Write($"+");
        }
        Console.WriteLine();
    }
}

void FillImage(int row, int col)
{
    if (pic[row, col] == 0)
    {
        pic[row, col] = 1;
        FillImage(row - 1, col);
        FillImage(row, col - 1);
        FillImage(row + 1, col);
        FillImage(row, col + 1);
    }
}

